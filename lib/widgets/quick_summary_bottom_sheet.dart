class QuickSummaryBottomSheet extends StatefulWidget {
  final String childId;
  final String childName;
  
  const QuickSummaryBottomSheet({
    super.key,
    required this.childId,
    required this.childName,
  });

  @override
  State<QuickSummaryBottomSheet> createState() => _QuickSummaryBottomSheetState();
}

class _QuickSummaryBottomSheetState extends State<QuickSummaryBottomSheet> {
  final UsageTrackingService _service = UsageTrackingService();
  final ScreenshotController _screenshotController = ScreenshotController();
  bool _isLoading = true;
  Map<String, dynamic> _data = {};
  
  @override
  void initState() {
    super.initState();
    _loadData();
  }

  Future<void> _loadData() async {
    setState(() => _isLoading = true);
    final data = await _service.getAutomaticQuickSummaryData(widget.childId);
    setState(() {
      _data = data;
      _isLoading = false;
    });
  }

  String _formatDuration(int minutes) {
    if (minutes < 60) return '$minutes min';
    final hours = minutes ~/ 60;
    final mins = minutes % 60;
    return mins > 0 ? '${hours}h ${mins}m' : '${hours}h';
  }

  Color _getWellnessColor(int score) {
    if (score >= 80) return Colors.green;
    if (score >= 60) return Colors.orange;
    return Colors.red;
  }

  String _getWellnessLabel(int score) {
    if (score >= 80) return 'Excellent';
    if (score >= 60) return 'Good';
    if (score >= 40) return 'Fair';
    return 'Needs Attention';
  }

  Future<void> _shareReportCard() async {
    try {
      final Uint8List? image = await _screenshotController.capture();
      if (image != null) {
        final directory = await getApplicationDocumentsDirectory();
        final fileName = 'daily_report_${widget.childName}_${DateTime.now().toIso8601String().split('T')[0]}.png';
        final file = File('${directory.path}/$fileName');
        await file.writeAsBytes(image);
        
        await Share.shareXFiles(
          [XFile(file.path)],
          text: '📊 ${widget.childName}\'s Daily Screen Time Report - ${DateTime.now().toString().split(' ')[0]}\n\nGenerated by STR App',
        );
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error sharing: $e')),
        );
      }
    }
  }

  Future<void> _exportToPDF() async {
    Navigator.pop(context);
    Navigator.push(
      context,
      MaterialPageRoute(
        builder: (context) => UsageAnalyticsScreen(
          childId: widget.childId,
          childName: widget.childName,
        ),
      ),
    );
  }

