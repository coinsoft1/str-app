  Widget _buildAIInsightCard() {
    final insight = _data['aiInsight'] ?? 'No insight available';
    IconData insightIcon = Icons.lightbulb;
    Color insightColor = Colors.blue;
    
    if (insight.contains('⚠️') || insight.contains('violation')) {
      insightIcon = Icons.warning;
      insightColor = Colors.orange;
    } else if (insight.contains('🌟') || insight.contains('Excellent')) {
      insightIcon = Icons.star;
      insightColor = Colors.green;
    } else if (insight.contains('📱') && insight.contains('active')) {
      insightIcon = Icons.phone_android;
      insightColor = Colors.purple;
    }
    
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: insightColor.withOpacity(0.1),
        borderRadius: BorderRadius.circular(12),
        border: Border(left: BorderSide(color: insightColor, width: 4)),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Icon(insightIcon, color: insightColor, size: 20),
              const SizedBox(width: 8),
              Text(
                'AI Insight',
                style: TextStyle(
                  fontWeight: FontWeight.bold,
                  color: insightColor.withOpacity(0.9),
                ),
              ),
            ],
          ),
          const SizedBox(height: 8),
          Text(
            insight,
            style: TextStyle(
              color: Colors.grey.shade800,
              height: 1.5,
              fontSize: 14,
            ),
          ),
        ],
      ),
    );
  }

