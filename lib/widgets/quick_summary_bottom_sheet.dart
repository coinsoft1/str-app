  Widget _buildHourlyHeatmap() {
    final hourly = _data['hourlyUsage'] as Map<dynamic, dynamic>? ?? {};
    if (hourly.isEmpty) return const SizedBox.shrink();
    
    // Convert to spots for chart
    final spots = <FlSpot>[];
    int maxMinutes = 0;
    for (int hour = 0; hour < 24; hour++) {
      final minutes = hourly[hour] ?? 0;
      spots.add(FlSpot(hour.toDouble(), minutes.toDouble()));
      if (minutes > maxMinutes) maxMinutes = minutes;
    }
    
    // Only show hours with data for cleaner UI
    final activeSpots = spots.where((s) => s.y > 0).toList();
    if (activeSpots.isEmpty) return const SizedBox.shrink();
    
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            const Text(
              'Hourly Activity',
              style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold),
            ),
            Text(
              'Peak usage times',
              style: TextStyle(fontSize: 12, color: Colors.grey.shade600),
            ),
          ],
        ),
        const SizedBox(height: 16),
        SizedBox(
          height: 180,
          child: LineChart(
            LineChartData(
              gridData: FlGridData(
                show: true,
                drawVerticalLine: false,
                horizontalInterval: maxMinutes > 0 ? maxMinutes / 4 : 10,
                getDrawingHorizontalLine: (value) {
                  return FlLine(
                    color: Colors.grey.shade200,
                    strokeWidth: 1,
                  );
                },
              ),
              titlesData: FlTitlesData(
                bottomTitles: AxisTitles(
                  sideTitles: SideTitles(
                    showTitles: true,
                    interval: 4,
                    getTitlesWidget: (value, meta) {
                      final hour = value.toInt();
                      return Padding(
                        padding: const EdgeInsets.only(top: 8),
                        child: Text(
                          '${hour.toString().padLeft(2, '0')}:00',
                          style: TextStyle(
                            fontSize: 11,
                            color: Colors.grey.shade600,
                            fontWeight: FontWeight.w500,
                          ),
                        ),
                      );
                    },
                  ),
                ),
                leftTitles: AxisTitles(
                  sideTitles: SideTitles(
                    showTitles: true,
                    interval: maxMinutes > 0 ? maxMinutes / 4 : 10,
                    getTitlesWidget: (value, meta) {
                      return Text(
                        '${value.toInt()}m',
                        style: TextStyle(
                          fontSize: 10,
                          color: Colors.grey.shade600,
                        ),
                      );
                    },
                    reservedSize: 40,
                  ),
                ),
                rightTitles: const AxisTitles(sideTitles: SideTitles(showTitles: false)),
                topTitles: const AxisTitles(sideTitles: SideTitles(showTitles: false)),
              ),
              borderData: FlBorderData(show: false),
              lineBarsData: [
                LineChartBarData(
                  spots: activeSpots,
                  isCurved: true,
                  color: Colors.purple,
                  barWidth: 4,
                  isStrokeCapRound: true,
                  dotData: FlDotData(
                    show: true,
                    getDotPainter: (spot, percent, bar, index) {
                      return FlDotCirclePainter(
                        radius: 4,
                        color: Colors.purple,
                        strokeWidth: 2,
                        strokeColor: Colors.white,
                      );
                    },
                  ),
                  belowBarData: BarAreaData(
                    show: true,
                    color: Colors.purple.withOpacity(0.15),
                  ),
                ),
              ],
              minY: 0,
              maxY: maxMinutes > 0 ? maxMinutes * 1.2 : 10,
            ),
          ),
        ),
      ],
    );
  }

