    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        const Text(
          'Content Breakdown',
          style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold),
        ),
        const SizedBox(height: 16),
        _buildCategoryBar('Educational', edu, total, Colors.green, '🎓', Icons.school),
        const SizedBox(height: 12),
        _buildCategoryBar('Entertainment', ent, total, Colors.orange, '🎮', Icons.videogame_asset),
        const SizedBox(height: 12),
        _buildCategoryBar('Utility', util, total, Colors.blue, '🔧', Icons.build),
      ],
    );
  }

