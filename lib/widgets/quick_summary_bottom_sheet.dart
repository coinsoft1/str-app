  Widget _buildComparisonStats() {
    final today = _data['totalMinutes'] ?? 0;
    final yesterday = _data['yesterdayTotal'] ?? 0;
    final weeklyAvg = _data['weeklyAverage'] ?? 0;
    
    final vsYesterday = today - yesterday;
    final vsWeekly = today - weeklyAvg;
    
    return Row(
      children: [
        Expanded(
          child: _buildComparisonCard(
            'vs Yesterday',
            vsYesterday,
            yesterday > 0,
          ),
        ),
        const SizedBox(width: 12),
        Expanded(
          child: _buildComparisonCard(
            'vs Weekly Avg',
            vsWeekly,
            weeklyAvg > 0,
          ),
        ),
      ],
    );
  }

  Widget _buildComparisonCard(String label, int difference, bool hasData) {
    final isPositive = difference > 0;
    final color = isPositive ? Colors.red : Colors.green;
    final icon = isPositive ? Icons.arrow_upward : Icons.arrow_downward;
    final text = hasData ? '${difference.abs()} min' : 'No data';
    final subtext = isPositive ? 'More than usual' : 'Less than usual';
    
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: Colors.grey.shade50,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: Colors.grey.shade200),
      ),
      child: Column(
        children: [
          Text(label, style: TextStyle(fontSize: 12, color: Colors.grey.shade600)),
          const SizedBox(height: 8),
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              if (hasData) ...[
                Icon(icon, size: 20, color: color),
                const SizedBox(width: 4),
              ],
              Text(
                text,
                style: TextStyle(
                  fontWeight: FontWeight.bold,
                  fontSize: 18,
                  color: hasData ? color : Colors.grey,
                ),
              ),
            ],
          ),
          if (hasData)
            Text(
              subtext,
              style: TextStyle(fontSize: 11, color: Colors.grey.shade600),
            ),
        ],
      ),
    );
  }

